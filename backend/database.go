type Store struct {
	db     *sql.DB
	logger *slog.Logger
}

func NewStore(dbPath string, logger *slog.Logger) (*Store, error) {
	if err := os.MkdirAll(filepath.Dir(dbPath), 0o755); err != nil {
		return nil, fmt.Errorf("create database directory: %w", err)
	}

	db, err := sql.Open("sqlite3", dbPath)
	if err != nil {
		return nil, fmt.Errorf("open database: %w", err)
	}

	db.SetMaxOpenConns(1)
	db.SetConnMaxLifetime(0)
	db.SetMaxIdleConns(1)

	store := &Store{db: db, logger: logger}
	if err := store.Init(); err != nil {
		_ = db.Close()
		return nil, err
	}

	return store, nil
}

func (s *Store) Init() error {
	if s.logger != nil {
		s.logger.Info("initializing database schema")
	}

	if _, err := s.db.Exec(`PRAGMA foreign_keys = ON`); err != nil {
		return fmt.Errorf("enable foreign keys: %w", err)
	}

	if _, err := s.db.Exec(createVideosTableSQL); err != nil {
		return fmt.Errorf("create videos table: %w", err)
	}
	if _, err := s.db.Exec(createVideosTitleIndexSQL); err != nil {
		return fmt.Errorf("create videos title index: %w", err)
	}
	if _, err := s.db.Exec(createUsersTableSQL); err != nil {
		return fmt.Errorf("create users table: %w", err)
	}
	if _, err := s.db.Exec(createSettingsTableSQL); err != nil {
		return fmt.Errorf("create settings table: %w", err)
	}
	if _, err := s.db.Exec(createPlaylistsTableSQL); err != nil {
		return fmt.Errorf("create playlists table: %w", err)
	}
	if _, err := s.db.Exec(createPlaylistItemsTableSQL); err != nil {
		return fmt.Errorf("create playlist items table: %w", err)
	}
	if _, err := s.db.Exec(createPlaylistItemsPlaylistIndexSQL); err != nil {
		return fmt.Errorf("create playlist items index: %w", err)
	}
	if _, err := s.db.Exec(createTorrentHistoryTableSQL); err != nil {
		return fmt.Errorf("create torrent history table: %w", err)
	}
	if _, err := s.db.Exec(createTorrentHistoryDownloadedAtIndexSQL); err != nil {
		return fmt.Errorf("create torrent history index: %w", err)
	}
	if _, err := s.db.Exec(`DELETE FROM settings WHERE key = 'hard_link_destination'`); err != nil {
		return fmt.Errorf("remove retired hardlink setting: %w", err)
	}

	if err := s.migrateTorrentHistoryStatus(); err != nil {
		return err
	}

	if err := s.migrateUserForcePasswordChange(); err != nil {
		return err
	}

	hasTitle, err := s.hasVideosColumn("title")
	if err != nil {
		return err
	}

	if !hasTitle {
		if _, err := s.db.Exec(`DROP TABLE IF EXISTS videos`); err != nil {
			return fmt.Errorf("drop old videos table: %w", err)
		}

		if _, err := s.db.Exec(createVideosTableSQL); err != nil {
			return fmt.Errorf("recreate videos table: %w", err)
		}
		if _, err := s.db.Exec(createVideosTitleIndexSQL); err != nil {
			return fmt.Errorf("recreate videos title index: %w", err)
		}

		if s.logger != nil {
			s.logger.Info("migrated videos table schema", "added_column", "title")
		}
	}

	hasQuality, err := s.hasVideosColumn("quality")
	if err != nil {
		return err
	}
	if !hasQuality {
		if _, err := s.db.Exec(`ALTER TABLE videos ADD COLUMN quality TEXT`); err != nil {
			return fmt.Errorf("add quality column: %w", err)
		}
		if s.logger != nil {
			s.logger.Info("migrated videos table schema", "added_column", "quality")
		}
	}

	hasTags, err := s.hasVideosColumn("tags_json")
	if err != nil {
		return err
	}
	if !hasTags {
		if _, err := s.db.Exec(`ALTER TABLE videos ADD COLUMN tags_json TEXT NOT NULL DEFAULT '[]'`); err != nil {
			return fmt.Errorf("add tags_json column: %w", err)
		}
		if s.logger != nil {
			s.logger.Info("migrated videos table schema", "added_column", "tags_json")
		}
	}

	hasActors, err := s.hasVideosColumn("actors_json")
	if err != nil {
		return err
	}
	if !hasActors {
		if _, err := s.db.Exec(`ALTER TABLE videos ADD COLUMN actors_json TEXT NOT NULL DEFAULT '[]'`); err != nil {
			return fmt.Errorf("add actors_json column: %w", err)
		}
		if s.logger != nil {
			s.logger.Info("migrated videos table schema", "added_column", "actors_json")
		}
	}

	return nil
}

func (s *Store) GetUserByUsername(username string) (User, error) {
	var user User
	var forceChange int
	err := s.db.QueryRow(`SELECT id, username, password_hash, force_password_change FROM users WHERE username = ?`, username).Scan(&user.ID, &user.Username, &user.Password, &forceChange)
	if err != nil {
		if errors.Is(err, sql.ErrNoRows) {
			return User{}, err
		}
		return User{}, fmt.Errorf("get user by username: %w", err)
	}
	user.ForcePasswordChange = forceChange != 0

	return user, nil
}

func (s *Store) UpdatePassword(username string, newHash string) error {
	result, err := s.db.Exec(`UPDATE users SET password_hash = ?, force_password_change = 0 WHERE username = ?`, newHash, username)
	if err != nil {
		return fmt.Errorf("update password: %w", err)
	}

	rowsAffected, err := result.RowsAffected()
	if err != nil {
		return fmt.Errorf("read updated rows: %w", err)
	}
	if rowsAffected == 0 {
		return sql.ErrNoRows
	}

	return nil
}

