func parseStringList(raw string) []string {
	if strings.TrimSpace(raw) == "" {
		return []string{}
	}

	var values []string
	if err := json.Unmarshal([]byte(raw), &values); err != nil {
		return []string{}
	}

	return sanitizeStringList(values)
}

func parseTorrentIndexers(raw string) ([]TorrentIndexer, error) {
	if strings.TrimSpace(raw) == "" {
		return []TorrentIndexer{}, nil
	}

	var indexers []TorrentIndexer
	if err := json.Unmarshal([]byte(raw), &indexers); err != nil {
		return nil, fmt.Errorf("decode torrent indexers: %w", err)
	}

	sanitized := make([]TorrentIndexer, 0, len(indexers))
	seen := make(map[string]struct{}, len(indexers))
	for _, indexer := range indexers {
		cleaned := sanitizeTorrentIndexer(indexer)
		if cleaned.ID == "" || cleaned.TorznabURL == "" || cleaned.APIKey == "" {
			continue
		}
		if _, exists := seen[cleaned.ID]; exists {
			continue
		}
		seen[cleaned.ID] = struct{}{}
		sanitized = append(sanitized, cleaned)
	}

	return sanitized, nil
}

func marshalTorrentIndexers(indexers []TorrentIndexer) (string, error) {
	sanitized := make([]TorrentIndexer, 0, len(indexers))
	seen := make(map[string]struct{}, len(indexers))
	for _, indexer := range indexers {
		cleaned := sanitizeTorrentIndexer(indexer)
		if cleaned.ID == "" || cleaned.TorznabURL == "" || cleaned.APIKey == "" {
			continue
		}
		if _, exists := seen[cleaned.ID]; exists {
			continue
		}
		seen[cleaned.ID] = struct{}{}
		sanitized = append(sanitized, cleaned)
	}

	data, err := json.Marshal(sanitized)
	if err != nil {
		return "", fmt.Errorf("encode torrent indexers: %w", err)
	}
	return string(data), nil
}

func sanitizeTorrentIndexer(indexer TorrentIndexer) TorrentIndexer {
	indexer.ID = strings.TrimSpace(indexer.ID)
	indexer.Label = strings.TrimSpace(indexer.Label)
	indexer.TorznabURL = strings.TrimSpace(indexer.TorznabURL)
	indexer.APIKey = strings.TrimSpace(indexer.APIKey)
	indexer.Tracker = strings.TrimSpace(indexer.Tracker)
	return indexer
}

func marshalStringList(values []string) (string, error) {
	data, err := json.Marshal(sanitizeStringList(values))
	if err != nil {
		return "", err
	}
	return string(data), nil
}

func sanitizeStringList(values []string) []string {
	seen := make(map[string]struct{}, len(values))
	cleaned := make([]string, 0, len(values))
	for _, value := range values {
		trimmed := strings.TrimSpace(value)
		if trimmed == "" {
			continue
		}
		key := strings.ToLower(trimmed)
		if _, exists := seen[key]; exists {
			continue
		}
		seen[key] = struct{}{}
		cleaned = append(cleaned, trimmed)
	}
	return cleaned
}

func uniqueInt64s(values []int64) []int64 {
	seen := make(map[int64]struct{}, len(values))
	unique := make([]int64, 0, len(values))
	for _, value := range values {
		if _, exists := seen[value]; exists {
			continue
		}
		seen[value] = struct{}{}
		unique = append(unique, value)
	}
	return unique
}

