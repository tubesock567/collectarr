func (s *Store) UpdateMetadataCatalog(addTags []string, removeTags []string, addActors []string, removeActors []string) (VideoMetadataOptionsResponse, error) {
	addTags = sanitizeStringList(addTags)
	removeTags = sanitizeStringList(removeTags)
	addActors = sanitizeStringList(addActors)
	removeActors = sanitizeStringList(removeActors)

	tx, err := s.db.Begin()
	if err != nil {
		return VideoMetadataOptionsResponse{}, fmt.Errorf("begin metadata catalog update: %w", err)
	}

	tagsCatalog, actorsCatalog, err := s.getMetadataCatalogTx(tx)
	if err != nil {
		_ = tx.Rollback()
		return VideoMetadataOptionsResponse{}, err
	}

	nextTagsCatalog := applyStringListChanges(tagsCatalog, addTags, removeTags)
	nextActorsCatalog := applyStringListChanges(actorsCatalog, addActors, removeActors)

	if err := s.setMetadataCatalogTx(tx, nextTagsCatalog, nextActorsCatalog); err != nil {
		_ = tx.Rollback()
		return VideoMetadataOptionsResponse{}, err
	}

	if len(removeTags) > 0 || len(removeActors) > 0 {
		if err := removeMetadataValuesFromAllVideosTx(tx, removeTags, removeActors); err != nil {
			_ = tx.Rollback()
			return VideoMetadataOptionsResponse{}, err
		}
	}

	if err := tx.Commit(); err != nil {
		return VideoMetadataOptionsResponse{}, fmt.Errorf("commit metadata catalog update: %w", err)
	}

	if s.logger != nil {
		s.logger.Info("metadata catalog updated", "tags_added", len(addTags), "tags_removed", len(removeTags), "actors_added", len(addActors), "actors_removed", len(removeActors))
	}

	return s.ListMetadataOptions()
}

