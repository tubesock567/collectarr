func removeMetadataValuesFromAllVideosTx(tx *sql.Tx, removeTags []string, removeActors []string) error {
	rows, err := tx.Query(`SELECT id, tags_json, actors_json FROM videos`)
	if err != nil {
		return fmt.Errorf("query videos for metadata catalog cleanup: %w", err)
	}
	defer rows.Close()

	type metadataRow struct {
		id         int64
		tagsJSON   string
		actorsJSON string
	}

	var updates []metadataRow
	for rows.Next() {
		var row metadataRow
		if err := rows.Scan(&row.id, &row.tagsJSON, &row.actorsJSON); err != nil {
			return fmt.Errorf("scan metadata cleanup row: %w", err)
		}
		updates = append(updates, row)
	}
	if err := rows.Err(); err != nil {
		return fmt.Errorf("iterate metadata cleanup rows: %w", err)
	}

	for _, row := range updates {
		nextTags := applyStringListChanges(parseStringList(row.tagsJSON), nil, removeTags)
		nextActors := applyStringListChanges(parseStringList(row.actorsJSON), nil, removeActors)

		tagsJSON, err := marshalStringList(nextTags)
		if err != nil {
			return fmt.Errorf("encode cleaned tags: %w", err)
		}
		actorsJSON, err := marshalStringList(nextActors)
		if err != nil {
			return fmt.Errorf("encode cleaned actors: %w", err)
		}

		if _, err := tx.Exec(`UPDATE videos SET tags_json = ?, actors_json = ? WHERE id = ?`, tagsJSON, actorsJSON, row.id); err != nil {
			return fmt.Errorf("update cleaned video metadata: %w", err)
		}
	}

	return nil
}

