func (s *Store) ListVideos() ([]Video, error) {
	rows, err := s.db.Query(`
		SELECT id, title, filename, path, quality, duration, tags_json, actors_json, date_added, date_scanned
		FROM videos
		ORDER BY date_added DESC, id DESC`)
	if err != nil {
		return nil, fmt.Errorf("query videos: %w", err)
	}
	defer rows.Close()

	var videos []Video
	for rows.Next() {
		video, err := scanVideoSummary(rows)
		if err != nil {
			return nil, fmt.Errorf("scan video summary: %w", err)
		}
		videos = append(videos, video)
	}

	if err := rows.Err(); err != nil {
		return nil, fmt.Errorf("iterate videos: %w", err)
	}

	return videos, nil
}

